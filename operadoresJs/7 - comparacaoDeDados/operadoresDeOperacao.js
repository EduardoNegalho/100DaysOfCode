// Operador de igualdade
console.log(10 == '10') // true
console.log(10 === '10') // false

console.log("-----")

// Operador de desigualdade (diferente)
console.log(10 != '10') // false
console.log(10 !== '10') // true

console.log("-----")

// Operador de comparação
console.log(10 > 10) // false
console.log(10 >= 10) // true
console.log(10 < 10) // false
console.log(10 <= 10) // true
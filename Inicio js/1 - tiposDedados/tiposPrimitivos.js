// Tipo number
let num1 = 10
console.log(typeof(num1))

let num2 = 17.5
console.log(typeof(num2))

// Tipo bigint
let num3 = 999n
console.log(typeof(num3))

// Tipo string
let nome = "Eduardo"
console.log(typeof(nome))

// Tpo boolean
let igual = 10 === 10 // true
let deferente = 10 === "10" // false
console.log(typeof(igual))

// Tipo null
let vazio = null; // saída no console é object
console.log(typeof(vazio))

// Tipo undefined
let indefinido;
console.log(typeof(indefinido))
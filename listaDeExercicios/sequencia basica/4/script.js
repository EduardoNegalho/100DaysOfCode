// Desenvolva um algoritmo que leia dois números inteiros e mostre o somatório
// entre eles.
// Ex:
// Digite um valor: 8
// Digite outro valor: 5
// A soma entre 8 e 5 é igual a 13

const number1 = parseInt(prompt("Digite o primeiro número:"))
const number2 = parseInt(prompt("Digite o segundo número:"))

const sum = number1 + number2

alert(`A soma entre ${number1} e ${number2} é igual a ${sum}`)